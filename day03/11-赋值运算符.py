# = 等号在计算机编程里，我们称之为赋值运算符，和数学里的等号有一定的区别

# 数学
# 3 = 3
# 4 = 4

# 计算机编程里，等号（赋值运算符）作用是将等号右边的值赋值给等号的做左边
# 等号的左边一定不能是常量或者表达式
a = 3   # 数字3 赋值给a

# 4 = a  错误写法
# 3 + 3 = m  错误写法
m = 3 + 3
print(m)

x = 1
# x = x + 2
x += 2  # x先加2，在等于
print(x)

x -= 1  # 2
print(x)

x *= 3
print(x)

x /= 3
print(x)

x **= 3
print(x)

x //= 3
print(x)

x %= 2
print(x)
