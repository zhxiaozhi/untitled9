# Python里支持很多算术运算符
# +加 -减 *乘 /除 **乘方 //整除 %取余
print(1 + 1)    # 2
print(4 - 2)    # 2
print(3 * 2)    # 6

# 在Python3中，两个整数相除，得到的结果会是一个float浮点数
print(6 / 2)    # 3.0
print(9 / 2)    # 4.5

print(3 ** 3)    # 27
print(81 ** 1 / 2)  # 40.5 , 81/2
print(81 ** (1 / 2))  # 9.0

print(10 / 3)   # 3.333333333333333
print(10 // 3)  # 3 整除，只取整数部分
print(10 % 3)   # 1 取余

print(1928 % 9276)  # 1928

